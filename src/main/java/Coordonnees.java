public class Coordonnees {
    private double X;
    private double Y;

    public Coordonnees() {
        setX(0);
        setY(0);
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }
}
